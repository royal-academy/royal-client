// ── Helper selectors — return plain string arrays ─────────────────────────────

export const getDivisions = (): string[] => BD_GEODATA.map((d) => d.value);

export const getDistricts = (divisionValue: string): string[] => {
  const div = BD_GEODATA.find((d) => d.value === divisionValue);
  return div ? div.districts.map((d) => d.value) : [];
};

export const getThanas = (
  divisionValue: string,
  districtValue: string,
): string[] => {
  const div = BD_GEODATA.find((d) => d.value === divisionValue);
  if (!div) return [];
  const dist = div.districts.find((d) => d.value === districtValue);
  return dist ? dist.thanas.map((t) => t.value) : [];
};
